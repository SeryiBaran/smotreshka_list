const r = s.match(/\d{1,3}-\d{1,3}/g).map((m) => {
  let c = []
  const [a, b] = m.split('-')
  for (let i = a; i <= b; i++) {
    c = [...c, i]
  }
  return [m, `${c.join('\n')}`]
})

